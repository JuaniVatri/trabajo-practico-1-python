# Declara dos variables numéricas (con el valor que desees), muestra por consola la 
# suma, resta, multiplicación, división y módulo (resto de la división).
num1=float(input ("Introduce el numero 1: "))
num2=float (input ("Introduce el numero 2: "))
suma= num1 + num2
resta= num1 - num2
mult= num1 * num2
div= num1 // num2
mod= num1 % num2
print ("La suma es",suma,", la resta es", resta,", La multiplicación es", mult, "La división es",div,", el módulo es", mod)
 
  